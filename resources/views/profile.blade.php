<h2><strong>Esquire Electronics Ltd.</strong></h2>
<p class="paragraph"><strong>Esquire Electronics Ltd.</strong>   is a Member Unit of Esquire Group, one of the most reputed conglomerates of the nation.<br>

	What started as a single showroom in 1974 has now grown into one of the most widely trusted and highly respected companies in Bangladesh. In the 40 years since its inception, Esquire Electronics Ltd. has specialized in consumer electronics and honed it’s services through perseverance and hard earned experience.<br><br>



	Esquire Electronics Ltd. is now the <strong>Authorized Sole Distributor</strong> for the Japanese electronics giants <strong>FUJISTU GENERAL</strong>  and <strong>SHARP</strong> . Esquire Electronics Ltd. also sells products from <strong>DANAAZ</strong>  – a relatively new brand established to cater to the mass population of our country.<br><br>



	We currently market the entire range of GENERAL RAC and PAC Air Conditioners. The engineering wing of Esquire Electronics Ltd also provide VRF Air Conditioning solutions by GENERAL.<br><br>


	Esquire Electronics Ltd. has earned the trust of many as a supplier of the entire range of SHARP Home Appliances including LED TVs, refrigerators, deep freezers, microwave ovens, washing machines, small residential air conditioning units with PCI and Inverter technology and other small home appliances.<br>

</p><br>
<h2><strong>Distribution Network:</strong></h2>

<p class="paragraph">Esquire Electronics Ltd. has an impressive network of 70 showrooms all over Bangladesh, complemented by 15 Customer Care Centers across the nation.<br><br>



	We have served over 60 lac customers of GENERAL and SHARP brands and we are widely recognized for our quality products, reliable prices and prompt and efficient after sales support.<br><br>



	Following the success of our physical retail network, we now provide e-commerce facilities for the convenience of people. Selecting and purchasing products have never been easier. But we didn’t stop there. We have partnered with several prominent online shops of Bangladesh to expand the reach of our products to an even larger audience. Now anyone can purchase our top notch products from the online services of their choice and rest assured knowing that the excellent reliability and service of Esquire Electronics Ltd. will follow.
</p><br>

